    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
        // TODO add your handling code here:
        switch (evento) {
            case ADDCONTATO:
                try {
                userCtrl.save(edtNome, edtSobrenome);
                Msg.Aviso(rootPane, "Novo usuário adicionado com sucesso!");
            } catch (Exception e) {
                Msg.Aviso(rootPane, e.getMessage());
            }            
            break;
            case UPDATECONTATO:
                atualizaContato();
                loadContato();
                break;
            case DELCONTATO:
                deletaContato();
                break;
            case ADDENDERECO:
                insereEndereco();
                loadTipoEnderco();
                break;
            case UPDATEENDERECO:
                atualizaEndereco();
                break;
            case ADDTIPOENDERECO:
                insereTipoEndereco();
                loadTipoEnderco();
                break;
            case UPDATETIPOENDERECO:
                if (tipoEndereco != null && edtTipoEndereco.getText().length() > 3) {
                    btnOK.setEnabled(false);
                    btnCancel.setEnabled(false);
                    edtTipoEndereco.setVisible(false);
                    lblMensagem.setText("");
                    edtNome.requestFocus();
                    
                    tipoEndereco.setTipoEndereco(edtTipoEndereco.getText().trim());
                    if (tipoEnderecoDao.altera(tipoEndereco)) {
                        JOptionPane.showMessageDialog(rootPane, "Tipo de endereço " + tipoEndereco.getTipoEndereco() + " alterado com sucesso!!!");
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "Erro ao alterar o tipo de endereço " + tipoEndereco.getTipoEndereco() + "!!!");
                    }
                    loadTipoEnderco();
                    edtTipoEndereco.setText("");
                    tipoEndereco = null;
                }
                break;
            case ADDTIPOTELEFONE:
                try {
                phoneTypeCtrl.save(edtTipoTelefone);
                lstTiposTelefones.setModel(phoneTypeCtrl);
                Msg.Aviso(rootPane, "Novo tipo de telefone adicionado com sucesso");
            } catch (Exception e) {
                Msg.Aviso(rootPane, e.getMessage());
            }
            break;
            case ADDTELEFONE:
                try {
                phoneCtrl.save(edtTelefone, phoneType);                
                Msg.Aviso(rootPane, "Novo telefone adicionado com sucesso");
            } catch (Exception e) {
                Msg.Aviso(rootPane, e.getMessage());
            }
            break;
        }
        //desativaOKCancel();
    }//GEN-LAST:event_btnOKActionPerformed

